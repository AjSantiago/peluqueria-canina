    public void cargarTabla() {
        //Definir modelo de la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            // Filas y columnas no editables
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        // Establecer nombres de las columnas
        String titulos[] = {"Num", "Nombre", "Raza", "Color", "Alérgico", "At. Esp.", "Nombre Duenio", "Celular", "Observaciones"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        // Carga de datos
        List<Mascota> listaMascotas = control.traerMascotas();
        
        if(listaMascotas != null){
            for(Mascota mascota : listaMascotas){
                Object[] objeto = {mascota.getNum_cliente(), mascota.getNombre(), mascota.getRaza(), mascota.getColor(), mascota.getAlergico(), mascota.getAtencion_especial(), mascota.getUnDuenio().getNombreDuenio(), mascota.getUnDuenio().getCelDuenio(), mascota.getObservaciones()};
                
                modeloTabla.addRow(objeto);
            }
        }
        
        tblMascotas.setModel(modeloTabla);
    }
