    private void cargarDatos(int num_cliente) {
        this.mascota = control.traerMascota(num_cliente);
        
        txtNombre.setText(mascota.getNombre());
        txtRaza.setText(mascota.getRaza());
        txtColor.setText(mascota.getColor());
        txtNombreDuenio.setText(mascota.getUnDuenio().getNombreDuenio());
        txtCelDuenio.setText(mascota.getUnDuenio().getCelDuenio());
        txtObservaciones.setText(mascota.getObservaciones());
        
        if(mascota.getAlergico().equals("Sí")){
                cmbAlergico.setSelectedIndex(1);

        } else if(mascota.getAlergico().equals("No")){
                cmbAlergico.setSelectedIndex(2);
        }
        
        if(mascota.getAtencion_especial().equals("Sí")){
                cmbAtenEsp.setSelectedIndex(1);

        } else if(mascota.getAtencion_especial().equals("No")){
                cmbAtenEsp.setSelectedIndex(2);
        }
    }
    
    public void mostrarMensaje(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }

        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
