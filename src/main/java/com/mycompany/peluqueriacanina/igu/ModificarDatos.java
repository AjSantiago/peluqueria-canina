    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombreMascota = txtNombre.getText();
        String raza = txtRaza.getText();
        String color  = txtColor.getText();
        String observaciones = txtObservaciones.getText();
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atenEsp = (String) cmbAtenEsp.getSelectedItem();
        
        String nombreDuenio = txtNombreDuenio.getText();
        String celDuenio = txtCelDuenio.getText();
        
        control.modificarMascota(mascota, nombreMascota, raza, color, observaciones, alergico, atenEsp, nombreDuenio, celDuenio);
        
        mostrarMensaje("Edición realizada correctamente", "Info", "Edición correcta");
        
        VerDatos pantalla = new VerDatos();
        pantalla.setVisible(true);
        pantalla.setLocationRelativeTo(null);
        
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

